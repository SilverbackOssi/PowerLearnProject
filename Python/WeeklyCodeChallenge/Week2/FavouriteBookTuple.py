'''
Create a tuple containing the names of five of your favorite books.
Then, use a for loop to print each book name on a separate line.
'''

# Tuple containing favorite books
favorite_books = (
    "The Alchemist",
    "1984",
    "To Kill a Mockingbird",
    "The Great Gatsby",
    "Moby-Dick"
)

